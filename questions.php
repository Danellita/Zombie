                    <h3 class='FAQ-subtitle'>Does coil count matter?</h3>
                        <p class='articles-texto'>Yes, it does. But this information tends to be manipulated and put into a context that would have you believe what each professes is the best. The gauge of the steel, the length and number of ply’s, the diameter, etc. all effect the quality. We need to take a common sense approach. Are some of these features worth the additional cost? </p>
