                <div class="letter"><span id="FAQ1"></span>
                    <h3 class='FAQ-subtitle'>Is napping good for you?</h3>
                    <p class='articles-texto'>While there is nothing wrong with napping --- it can actually be refreshing and a jump charge on energy if not longer than 45 minutes --- it can be a sign that you are not getting enough sleep at night, or not getting the deep, restful sleep required.  An occasional nap is natural, but can be a warning sign if needed on a regular basis.</p>
                </div>
                    <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ2"></span>
                    <h3 class='FAQ-subtitle'>What's the right amount of sleep?</h3>
                        <p class='articles-texto'>The average person needs 7-8 hours per night. If sleepy during the day, or needing more on the weekends, then more time is probably needed on a daily basis during the week.  More than 10 hours can be a sign of another problem. </p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ3"></span>
                    <h3 class='FAQ-subtitle'>I have no time to sleep!</h3>
                        <div class='faq-image-block'>
                            <img class='faq-image1' src="/images/mattress_faq_info-how-much-sleep-web.jpg" alt='sleep disoder'>
                        </div> 
                    <p class='articles-texto'>Sleep affects every aspect of your life. You are only as good as the previous night’s sleep. If you cannot get the quantity, at least get the quality. To do this, you must be healthy in all other areas --- by exercising regularly and eating a healthy diet.  Avoid caffeine, alcohol and tobacco products late in the day.  Sleep in a dark, quiet, cool room in a mattress perfect for your body type and needs.</p>
                </div>
                <a href='#top'>Top</a>
                
                
                <div class="letter"><span id="FAQ4"></span>
                    <h3 class='FAQ-subtitle'>How can I get a better night’s sleep?</h3>
                        <p class='articles-texto'>Keep the same schedule every day, even on week-ends. This will keep your biological clock regular. Develop a sleep ritual. Unwind early in the evening so distractions get dissipated. </p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ5"></span>
                    <h3 class='FAQ-subtitle'>How does my mattress affect my sleep?</h3>
                        <p class='articles-texto'>Your mattress is the most crucial physical part of your sleep. Even if all psychological and health aspects are good, a poor mattress can rob you of sleep. If you cannot fall asleep on a comfortable and supportive mattress, you cannot stay asleep, which is how you re-energize. You know your mattress is right if you do not toss and turn to get comfortable. The longer you stay in the same position is the key. Shifting positions interrupts the deep sleep process.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ6"></span>
                    <h3 class='FAQ-subtitle'>Do I have a sleep disorder?</h3>
                        <div class='faq-image-block'>
                            <img class='faq-image2' src="/images/mattress_faq_info_apnea_web.jpg" alt='sleep disoder'>
                        </div>
                        <p class='articles-texto'>If you have met all the crucial sleep requirements with a healthy diet, exercise, sleep routine, a healthy sleep environment, and have the perfect mattress for comfort and support, and are still not getting a good night’s sleep, you may have a sleep disorder, such as sleep apnea. You may need to consult a medical professional, such as an ENT.</p>
                
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ7"></span>
                    <h3 class='FAQ-subtitle'>How do I know which is the best mattress for me?</h3>
                        <p class='articles-texto'>Mattress selection is very personalized.  Different body types, weights, sleep position preferences, and medical conditions affect your choice. To determine which mattress is best for you, visit a reputable mattress professional and take part in the “rest test”. You will be asked to lie on the four basic mattress types: firm, pillow-top (the softest choice), plush (the medium, most adapting choice), and a memory foam. Pay attention to the three crucial features: support, comfort, and space. An analysis will be made by a qualified sleep consultant, and the best mattress choice for you and your partner will be diagnosed. This may be a combination of the basics, such as a firm pillow-top for a couple who vary in body type and requirements.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ8"></span>
                    <h3 class='FAQ-subtitle'>Can I make up for lost sleep?</h3>
                    <p class='articles-texto'>No. Sleep cannot be made up. If you sleep more on the week-ends, then you may have a “sleep debt”, which accumulates when you don’t get enough sleep. To reduce this debt is to sleep as much as your body requires every night. Go to sleep and rise the same time every day.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ9"></span>
                    <h3 class='FAQ-subtitle'>What should I look for in a mattress?</h3>
                        <div class='faq-image-block'>
                            <img class='faq-image1' src="/images/mattress_faq_info_good_mattress-web.jpg" alt='good_mattress'>
                        </div>
                        <p class='articles-texto'>The four points to look for are support, comfort, space, and matching sets. The crucial point is perfect spinal alignment. While on your back, you should receive proper lumbar and neck support. While on your side, your spine should be totally straight from neck to tail bone. While on your stomach (if you must), your spine should not be concave. With support covered, comfort it key. You should be able to quickly fall asleep (due to comfort), and stay asleep with minimal movement (due to support), and have enough space if with a partner. A mattress set should be compatible. </p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ10"></span>
                    <h3 class='FAQ-subtitle'>How do I prepare for purchasing a mattress?</h3>
                        
                    <p class='articles-texto'>Be well rested, have a good meal, and wear comfortable clothing. Plan to spend an hour or so in the sleep gallery.  Your choices should be narrowed down to 2 or 3 mattresses.  Lie on each for 15 minutes. Whichever you move the least on is the choice for you.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ11"></span>
                    <h3 class='FAQ-subtitle'>How often should a mattress be replaced?</h3>
                    <p class='articles-texto'>On average, a mattress generally should be replaced every 8-to-10 years. Of course, there are variables, such as the quality of the mattress (initial investment), the pressure put on it over the years and how often the mattress is used. A guest room or vacation home mattress may last longer because it is not used every night. However, bacteria and dust mites accumulate in your mattress, and should be replaced after 10 years. To prolong mattress life, spin it every 6-to-12 months to prevent body impressions, especially when partners are of different weights and types.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ12"></span>
                    <h3 class='FAQ-subtitle'>Does age affect mattress choice?</h3>
                        <p class='articles-texto'>Yes. As we age, our muscle mass decreases, and our bones become more brittle. A softer mattress is required. The habit of sleeping on a mattress that the body has gotten used to, does not mean that it is the best one for the individual. Humans are creatures of habit. Our bodies change over time, and with it comes different physical requirements. The mind may not want to accept this, but it is a necessity for proper fit. </p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ13"></span>
                    <h3 class='FAQ-subtitle'>Is it legal to sell used mattresses?</h3>
                        <p class='articles-texto'>The mattress industry, in certain states, can sell a used mattress as “slightly used” or “almost new” by obtaining a license to do so. Your mattress is your most intimate possession. You do not know the history of a used mattress --- where it has been or who or what has had contact with it.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ14"></span>
                    <h3 class='FAQ-subtitle'>How do I know if I choose the right mattress? How long should the trial period be?</h3>
                        <div class='faq-image-block'>
                            <img class='faq-image2' src="/images/mattress_faq_info_mattress_web.jpg" alt='best mattresses'>
                        </div>    
                    <p class='articles-texto'>You should give yourself at least 30 days to adjust to a new mattress. You may be lucky and know sooner. Any longer is unnecessary, as your body will not tolerate an unfit mattress after that time period. Manufacturers and retailers that allow 100 days or more are hoping that you adapt and become accustomed to your mattress, and will not bother to exchange it.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ15"></span>
                    <h3 class='FAQ-subtitle'>Will my new mattress feel the same as it did in the store?</h3>
                        
                        <p class='articles-texto'>There is a breaking-in period and your new mattress may feel a bit firmer than the one you tired in your place of purchase. </p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ16"></span>
                    <h3 class='FAQ-subtitle'>What is hypoallergenic?</h3>
                    <p class='articles-texto'>A hypoallergenic mattress is a specially designed mattress which is supposed to prevent allergies. Usually this type of mattress is tightly sealed to prevent dead skin and body oils from penetrating the mattress and attracting dust mites, along with other microscopic organisms that can cause allergic reactions. A hypoallergenic mattress works just like a regular mattress, using a series of materials to create a comfortable sleeping surface. Many hospitals use basic hypoallergenic mattresses to keep their patients healthy. These mattresses may have filling identical to an ordinary mattress, but a thick layer of plastic, latex, or vinyl on the outside that prevents materials outside the mattress from seeping in. More expensive mattresses are fully hypoallergenic, using neutral materials which will not aggravate allergies. A less costly alternative to a hypoallergenic mattress is a mattress wrap, which seals the mattress in a layer of protective material.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ17"></span>
                    <h3 class='FAQ-subtitle'>Why an adjustable base?</h3>
                        
                    <p class='articles-texto'>An adjustable base enables you to position yourself in a “zero-gravity” position, the natural human posture. In space, our bodies would naturally be in a semi-fetal position. It relieves stress on the back, neck, hips and legs. Consider this: Hospitals regard this as a healing positon, and have beds that raise your head and feet. A dentist puts you in this position to prevent movement in the chair, when sharp instruments are in your mouth. You raise your feet to relieve swelling and throbbing of the lower extremities. You raise your head when you have a cold, sinus infection, reflux, and snore.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="FAQ18"></span>
                    <h3 class='FAQ-subtitle'>Why is there such a wide price range in mattresses? Is it worth it?</h3>
                    <p class='articles-texto'>Your mattress is your most important investment. It is an investment in YOU, in your body, for your well-being. You are only as good as the previous night’s sleep. You literally spend 1/3 of your life in bed, 1/3 of every 24 hours, of every day of every week throughout every year. Without the rejuvenating requirements of a good night’s sleep, you are not living to 100% of your potential --- socially, professionally, intimately and philosophically. It is worth the investment. The delight in getting a supposedly good deal is quickly lost with the realization of a poor investment. But the best choice for you does not have to be the most expensive. Just like a good pair of supportive shoes, one may get by on a simple pair of loafers, while another may require orthopedic shoes. Consider this: You may spend thousands of dollars on the perfect dining room set that you use 20 times in a 10-year period. You may spend hundreds of dollars on an outfit for a special occasion that you wear once or twice and then remains in the back of your closet. And how much do you spend on coffee each year? These are not cost-effective. But we all have done it. Is our health and overall well-being not worth the investment?</p>
                </div>
                <a href='#top'>Top</a>
                
