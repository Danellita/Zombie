                <div class="letter"><span id="FAQ23"></span>
                    <h3 class='FAQ-subtitle'>Is a high quality, high cost mattress really worth it?</h3>
                    <p class='articles-texto'>You spend one third of your life in your mattress. This is where you rejuvenate, regenerate, and process everything from your day. Not having the right mattress can be the difference between success and survival. There are two types of people --- those who go with and those who go without. The person who goes without burns out much more quickly and is unhealthy, while the person who paces him/herself and enjoys life lives longer and has much more enjoyment. A good mattress is just as important as diet and exercise. It completes the physical and mental health requirement. This is a 7-to-10 year investment that will be used daily --- it is worth as much as you are.</p>
                </div>
                
                <div class="letter"><span id="FAQ24"></span>
                    <h3 class='FAQ-subtitle'>Do I need all the bells and whistles?</h3>
                    <p class='articles-texto'>What you need is the BEST mattress that supports and satisfies YOUR needs. It doesn’t have to be the thickest or the most expensive. It doesn’t have to have the latest technological gadgets. It does have to induce sleep with the comfort factor and provide prolonged sleep (7-to-9 hours) with the support factor, so that your sleep is restorative.</p>
                    <p class='articles-texto'>We need to take a common sense approach. Are some of these features worth the additional cost? Consider this analogy: An extravagant foreign sports car that has the highest horsepower of any vehicle on the planet would have us believe that this is the best that money can buy. Perhaps it is, but is it necessary? Where, when and how often is that speed actually required? Where is it legal? Who would be driving?</p>
