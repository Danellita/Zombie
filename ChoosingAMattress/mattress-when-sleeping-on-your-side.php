                <p class='school-texto'>The back and hips are the biggest muscle groups of the body, supporting its weight. When lying on your side, body weight is rested on the ankles, knees, hips and ribs -- which have no muscle -- and on the shoulder. These are pressure points. The small shoulder muscle is absorbing the weight of the entire body. If not properly supported, by being recessed into the mattress, the pressure on this muscle will decrease blood circulation causing a pinched nerve resulting in numbness and tingling in the fingertips.  It will also cause miss-alignment, resulting in neck and back pain. The goal here is to have the spine aligned in a perfectly straight line, from neck to tail bone. A curved spine is the result of a too soft or too firm mattress.</p>
                
                
