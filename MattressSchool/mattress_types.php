                <div class="letter"><span id="Coil"></span>
                    <h3 class='articles-subtitle'>Coil or Innerspring:</h3>
                        <p class='articles-texto'>Spirals of wires that form the supportive core of a mattress. There are two types. The preferred of the two is the individually wrapped. These can be a single coil, a coil-within a coil (two coils) or twisted coils (2- and 3-ply). Higher quality mattresses consist of individually wrapped coils. A better quality mattress will offer the most flexibility and multiple support features by alternating different types of individually wrapped coils.  The second type is the continuous coil, wherein each row of coils is constructed from a single wire shaped into a series of S-shaped ringlets. This is a more cost effective way of producing a mattress. However, this technique provides for a very “bouncy” mattress with overall support, instead of “specific” support. Both provide a “sleeping on top” feeling.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="Foam"></span>
                    <h3 class='articles-subtitle'>Memory Foam:</h3>
                        <p class='articles-texto'>High-density polyurethane foam known for its slow recovery time and its sensitivity to both weight and temperature, giving it a delayed resiliency and allowing it to conform very closely to the shape of your body. Available in many depths and densities, as well as different levels of firmness. Reduces pressure points on the body. Provides a “sleeping in” feeling. </p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="Latex"></span>
                    <h3 class='articles-subtitle' >Latex:</h3>
                        <p class='articles-texto'>Spongy material produced from either natural or synthetic rubber, which has pressure relieving properties like memory foam. However, it has a much faster reaction time. Used primarily in premium mattresses.</p>
                </div>
                <a href='#top'>Top</a>
                
                
                <div class="letter"><span id="Hybrid"></span>
                    <h3 class='articles-subtitle'>Hybrid:</h3>
                        <p class='articles-texto'>Mattress that combines an innerspring unit with specialty foams such as memory foam and latex. The springs are relatively shorter than a typical innerspring mattress. It provides the conforming benefits of a memory foam, with the added buoyancy not found in a true memory foam.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="Air"></span>
                    <h3 class='articles-subtitle'>Air:</h3>
                        <p class='articles-texto'>Air filled chambers at its core for support. Can customize the firmness or softness of a mattress by filling and releasing air. Most offer separate chambers in the left and right of the mattress for individual comfort on each side of the mattress.</p>
