                <div class="letter">
                    <h3 class='articles-subtitle'>Standard Box Spring</h3>
                        <p class='articles-texto'>A foundation of support (base) for a mattress, consisting of a wood or metal frame and a dust cover wrapped around. It serves as a shock absorber, distributes weight, and paired with the specifics and weight of the mattress. Required if the mattress sits on a slatted bed or frame. Available in the standard 9” height or a 5” low-profile height.</p>
                </div>
                
                
                <div class="letter">
                    <h3 class='articles-subtitle'>Bunky Board</h3>
                        <p class='articles-texto'>A thin piece of plywood covered with foam and fabric, with a 2” total height. Typically used with a very thick mattress, or a storage bed.</p>
                </div>
                
                
                <div class="letter">
                    <h3 class='articles-subtitle' >Adjustable Base</h3>
                    <p class='articles-texto'>Electronic moving base which raises the top and bottom of a mattress (head and feet) to relieve body pressure. Puts you into a zero-gravity position --- the natural anatomical position. This position stretches the lower spine relieving pain caused by herniated discs. It also relieves tension in the neck and shoulders by supporting the head. Working with gravity, it increases circulation of blood in the legs, reduces pressure on the lungs, drains fluids from the head, and opens the airway to help reduce or eliminate snoring.</p>
