            <div class="letter"><span id="A"></span>
                <h3 class='school-subtitle'><span id="A"></span>Adjustable Bed (Base)</h3>
                <p class='school-texto'>Foundation or base distinguished by its ability to bend and elevate the head and feet (two-part articulation) into upright positions. The head can typically be raised 65 degrees, for reading, TV viewing, eating, and working on a laptop. Raising the head above the heart and lungs is also known as “the healing position” that can make deep breathing less labored, help with sinus conditions, acid reflux, sleep apnea, and snoring. It also reduces stress on the neck and shoulders due to the support of the human head (which weighs between 8- and 12 pounds). The feet can typically be raised to 35 degrees, perfect for increasing circulation, and relieving hip pressure. Features include massage, memory settings, snore relief, lumbar support, and monitoring of heart beat and breathing.</p>
            </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Air Mattress</h3>
                <p class='school-texto'>Mattress distinguished by its use of an air-filled core, with chambers rather coils, for support. May be upholstered with cushioning, quilting and/or ticking.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Anti-Microbial Fiber and Foam</h3>
                <p class='school-texto'>Fiber or foam that has been treated to prevent the growth of bacteria, dust mites, mold, and fungi.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="B"></span>
                <h3 class='school-subtitle'>Body Impressions</h3>
                <p class='school-texto'>Indentations on the surface of the mattress resulting from the normal compression of mattress quilting and cushioning layers over time. Industry standard not to exceed 1-1/2 inches. Often mistakenly characterized as sagging. </p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Box Spring</h3>
                <p class='school-texto'>A foundation of support (base) for a mattress, consisting of a wood or metal  frame and a dust cover wrapped around. It serves as a shock absorber, distributes weight, and paired with the specifics and weight of the mattress. Required if the mattress sits on a slatted bed or frame.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="C"></span>
                <h3 class='school-subtitle'>Certified Organic Mattress</h3>
                <p class='school-texto'>A mattress "USDA Organic" is strictly forbidden by USDA regulations. But mattresses can contain organic ingredients and be certified by GOTS (Global Organic Textile Standards) for fiber and textile handling and production. </p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Coils</h3>
                <p class='school-texto'>Spirals of wires that form the supportive core of an innerspring mattress. May be individually wrapped coils or a coil-within a coil (two coils) or twisted coils (2- and 3-ply).</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Coil Count</h3>
                <p class='school-texto'>Number of individually wrapped coils in an innerspring mattress. The count can effect weight distribution and support.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Comfort</h3>
                <p class='school-texto'>Way in which the surface of the mattress feels against your body. Can be thought of as the "hardness" or "softness". It is this “feeling” that will not only allow you to quickly fall asleep, but more importantly, allow you to stay asleep and enter and stay in the REM cycle of sleep. This is key for all the benefits of a good, rejuvenated night’s sleep.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Comfort Layers</h3>
                <p class='school-texto'>Layers of upholstery surrounding the core that give the mattress surface a comfortable feel. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Continuous Coil</h3>
                <p class='school-texto'>An innerspring mattress coil design wherein each row of coils is constructed from a single wire shaped into a series of S-shaped ringlets. The concept behind the design is that by attaching each coil to its neighbors, the innerspring unit will have improved strength, stability, and durability. This is a more cost effective way of producing a mattress. However, this technique provides for a very “bouncy” mattress with overall support, instead of “specific” support in the major areas --- the lumbar, between the shoulders, the neck, and behind the knees. This type of coil does not conform to the contour of the body and does not prohibit the “transfer of motion”.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Continuous Pour</h3>
                <p class='school-texto'>Process for making latex foam with a blend of dunlop and talalay properties. Liquid latex is "whipped" with air until it becomes wet foam, and then poured onto a slow-moving conveyor belt (rather than into a mold).  The conveyor is then moved through a vulcanization process, resulting in one long continuous sheet of latex foam. The process can be used for both natural and synthetic latex, and because the resulting foam slabs are thinner than with dunlop, the difference in density between top and bottom is not as great, and a broader range of softness levels and densities can be produced. Talalay lacks the uniform density and softness range, but produces larger sheets that can cover a king or queen mattress without any glues.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Convoluted Foam (Egg-Crate)</h3>
                <p class='school-texto'>Foam that is specially cut to produce hills and valleys, giving gentle softness and more surface comfort. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Core</h3>
                <p class='school-texto'>The underlying support layers of a mattress, whether innerspring or foam. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Cover</h3>
                <p class='school-texto'>Fabric encasing the mattress. (Also referred to as ticking).</p> 
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Crown</h3>
                <p class='school-texto'>Condition wherein a mattress is slightly higher in the middle than on the sides, creating a convex surface. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Cushioning</h3>
                <p class='school-texto'>Layers of material that lie between the insulation and the quilting of an innerspring mattress. Cushioning materials include latex, memory foam, convoluted foam, polyester, wool, goose down, silk and cashmere. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="D"></span>
                <h3 class='school-subtitle'>Density</h3>
                <p class='school-texto'>Measurement of weight over volume, typically referred to in pounds per cubic foot. Correlates with the durability of a foam mattress.</p> 
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Dunlop</h3>
                <p class='school-texto'>Process for making latex foam known for its high density, progressive compression characteristics, and lower cost. Liquid latex is "whipped" with air 
                    until it becomes wet foam, at which time it is poured into a mold, hardened, and vulcanized. The dunlop process can be used for both natural and synthetic latex, and tends to result in slightly firmer and heavier (more dense) latex than the talalay process. Also, because it is not vacuum-sealed, the heaviest particles sink to the bottom prior to hardening, meaning the bottom of a dunlop latex sheet tends to be firmer and more dense than the top (giving dunlop an appealing support quality known as "progressive compression"). For this reason, as well as its lower cost, dunlop tends to be used more often as a support layer than as a comfort layer.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="E"></span>
                <h3 class='school-subtitle'>Edge Break-Down</h3>
                <p class='school-texto'>The loss of support along the outer edges of a mattress, commonly experienced in older mattresses. The sensation is that one is rolling off the side of the bed. The latest technology addresses this with denser cellulose edging, and incorporating mini-coils that expand to the mattress’s edge.</p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Edge Coils</h3>
                <p class='school-texto'>Addition of heavier coils placed along the edges of a mattress to provide extra support and protection against edge break-down. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Euro-Top</h3>
                <p class='school-texto'>Layer of additional padding sewn on top of a mattress. Similar to a pillow-top, but attached more firmly to the mattress. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="F"></span>
                <h3 class='school-subtitle'>Fatigue</h3>
                <p class='school-texto'>State of a mattress when it can no longer adequately support a sleeper's weight. Typically results from a softening or compressing of the coils. </p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Fiber</h3>
                <p class='school-texto'>Strands of material, made from natural or synthetic elements, which are used extensively in mattresses. Fiber strands have air between them, meaning that fiber tends to compress over time. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Firmness</h3>
                <p class='school-texto'>Compression resistance of a mattress. Typically used in reference to a mattress's support, as provided by the coils of an innerspring mattress, though sometimes also used to describe the hardness of a mattress's surface.  A mattress’s firmness is 
                    felt by the construction of the inner coils, and well as the arrangement of the layers of foam, and the tufting. (A tighter pulled covering with a denser tuft will result in a firmer, less lofty, feel).</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Foam Encasing</h3>
                <p class='school-texto'>Stiff foam rails along the interior perimeter of a mattress, generally used to protect against edge break-down.</p> 
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Foundation</h3>
                <p class='school-texto'>Any base or support placed beneath a mattress. Term often used in reference to a box spring. Made of wood or a combination of wood and steel, or may be an adjustable base.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="G"></span>
                <h3 class='school-subtitle'>Gauge</h3>
                <p class='school-texto'>Measurement of the thickness of the wire used in a coil. The lower the gauge, the thicker/heavier the wire.</p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Gel Foam</h3>
                <p class='school-texto'>Visco-elastic foam containing “beads” or particles of semi-solid-gel. Also called “gel-infused foam”. Gel promotes body temperature control and extra support.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="H"></span>
                <h3 class='school-subtitle'>Hammocking</h3>
                <p class='school-texto'>An undesirable characteristic associated with worn-out or low-end mattresses. When weight is placed in the center, the corners tend to rise and bow in response to deep compression, much like a hammock.</p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Hybrid</h3>
                <p class='school-texto'>Mattress that combines an innerspring unit with specialty foams such as memory foam and latex. The springs are relatively shorter than a typical innerspring mattress. The choice made when a full commitment to a memory foam is not desired; it provides the sensation of the body conforming nature of a memory foam, but adds a bit of “push back” to prevent that “sleeping in the mattress” feeling.</p> 
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="I"></span>
                <h3 class='school-subtitle'>Ideal Weight Distribution</h3>
                <p class='school-texto'>Equalization of support in such a way as to eliminate pressure points that cause discomfort resulting in tossing and turning. </p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Innerspring Mattress</h3>
                <p class='school-texto'>Mattress constructed around a core of tempered coil springs. An innerspring mattress is topped by several layers of foam to provide comfort. Individually wrapped coils work both together and separately to customize the mattress to the specific body shape and weight of the individual and support where needed. Also reduces the transfer of motion, as the individual coils act independently of each other.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="L"></span>
                <h3 class='school-subtitle'>Latex</h3>
                <p class='school-texto'>Spongy material produced from either natural or synthetic rubber. Used primarily in premium mattresses.</p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Low-Profile Foundation</h3>
                <p class='school-texto'>Lower height foundation made to accompany an extra-thick mattress or bed design. Typically 5" high. (A 2” high “bunky board” may also be used for very low height requirements).</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Luxury Mattress</h3>
                <p class='school-texto'>High-end mattress produced with specialized materials, hand-crafting, extra features, and/or customization. </p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="M"></span>
                <h3 class='school-subtitle'>Marshall</h3>
                <p class='school-texto'>Innerspring construction in which thin gauge, barrel-shaped, knotless coils are encased in fabric pockets.  Also known as “pocketed coils”. </p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Memory Foam (Viscose-elastic foam)</h3>
                <p class='school-texto'>High-density polyurethane foam known for its slow recovery time and its sensitivity to both weight and temperature, giving it a delayed resiliency and allowing it to conform very closely to the shape of your body. Available in many depths and densities, as well as different levels of firmness.</p> 
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Motion Separation</h3>
                <p class='school-texto'>Degree to which movement on one side of the bed can be felt on the other size of the bed. Also known as "Transfer of Motion".</p> 
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="N"></span>
                <h3 class='school-subtitle'>Natural Latex</h3>
                <p class='school-texto'>Latex produced from serum of the rubber tree. Known for its softness and elasticity, as well as its biodegradability and inherent resistance to bacteria, mold and dust mites.</p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="O"></span>
                <h3 class='school-subtitle'>Off-Gassing</h3>
                <p class='school-texto'>Evaporation of chemicals from a material into the air. The EPA warns that off-gassing of certain chemicals can potentially be harmful to humans. </p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Organic Latex</h3>
                <p class='school-texto'>There is no such thing as an “organic latex” mattress. The only products that can be certified organic are foods and fiber. Being a manufactured product, certification does not exist for latex.</p> 
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Orthopedic</h3>
                <p class='school-texto'>Term that implies proper postural alignment and support with a degree of comfort to contour to the body.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Organic Mattress</h3>
                <p class='school-texto'>Mattress made with natural materials, designed to minimize exposure to harmful chemicals or other irritants.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="P"></span>
                <h3 class='school-subtitle'>PBDE's (Polybrominated Diphenylethers)</h3>
                <p class='school-texto'>Chemical used as flame retardants in foams, plastics and fabrics which slows the ignition time and rate of the spread of fire.</p>
                </div>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Phase-Change Material</h3>
                <p class='school-texto'>PCMs are organic and inorganic compounds that store and release heat as they melt and solidify at certain temperatures. In a mattress, will absorb body heat and store it until needed when the body cools, keeping one’s body temperate even throughout the night.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Pillow-Top</h3>
                <p class='school-texto'>Thick, soft, cushy layer of foam, cotton, wool and/or other fibers that is attached loosely over the entire surface of the mattress.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Pincore Holes</h3>
                <p class='school-texto'>Deep, cylindrical holes spread throughout a latex mattress to help soften its feel.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Plush</h3>
                <p class='school-texto'>Softer level of surface comfort. It is softer than a firm mattress, and firmer than a pillow-top. Loosely considered the “medium” choice.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Pocketed Coils</h3>
                <p class='school-texto'>Type of innerspring mattress coils that are individually wrapped in separate fabric pockets, providing more motion separation than other types of innerspring coils.  It minimizes transfer of motion.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Pocketing</h3>
                <p class='school-texto'>Form of mattress wear wherein layers of padding begin to settle into the coils.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Pressure Points</h3>
                <p class='school-texto'>Specific places on the body where blood flow through the capillaries is restricted due to pressure from the sleeping surface against that area. Known to cause discomfort, numbness, as well as tossing and turning.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <h3 class='school-subtitle'>Prorated Warranty</h3>
                <p class='school-texto'>Warranty that does not cover the full mattress purchase price or replacement value after a certain period of time, requiring the customer to pay a portion of the original price to have it replaced. </p>
                <a href='#top'>Top</a>
                <hr>
                <h3 class='school-subtitle'>Pure Latex</h3>
                <p class='school-texto'>Latex that does not include fillers.</p>
                <a href='#top'>Top</a>
                <hr>
                
                <div class="letter"><span id="R"></span>
                <h3 class='school-subtitle'>Recovery Time</h3>
                <p class='school-texto'>Speed which a material returns to its original shape after being compressed.</p>
                </div>
