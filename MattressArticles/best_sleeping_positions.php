                <p class='articles-main-paragraph'>Regardless of health benefits, people sleep in the position they find the most comfortable.</p>
                
                <div class='articles-image'>
                    <img id='article2-image' src="/images/Articles-Mattress-best-sleeping-positions.jpg" alt='the huge mattress business'>
