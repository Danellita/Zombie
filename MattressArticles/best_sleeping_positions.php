                <p class='articles-texto'>Whether it’s back, side, or stomach, people tend to wake up in the position that their bodies naturally snooze in. Unless a doctor specifically recommends switching, it’s probably best to keep doing what feels right. With the stress of daily life, we crave a good night’s sleep, whatever it takes.</p> 
                <p class='articles-texto'>However, if you consciously want to maximize your overall body health, you may opt to change your sleeping habits. People don’t think about sleeping positions when desperate to just sleep. But proper sleep position allows you to accomplish maximum sleep requirements and benefits.</p>
                <p class='articles-texto'>It all starts with the spine. Stress on the spine increases stress on other structures in your body. It is a pipeline for your nerves and can cause pain anywhere in your body. Nerve stress and compression cause tingling and numbness.</p>
                <p class='articles-texto'>What positions cause the most stress?</p>
                
                <h3 class='articles-subtitle'>Stomach Sleeping:</h3>
                <p class='articles-texto'>Many stomach sleepers experience some type of pain. Whether it’s in the neck, back, or joints, this pain can affect how much sleep you get. More pain means you’re more likely to wake up during the night and feel less rested in the morning.</p>
                <p class='articles-texto'>Sleeping on your stomach places a strain on your back and spine. It flattens the natural curve of the spine. This is because most of your weight is in the middle of your body. You also must turn your head to the side, which puts your head and spine out of alignment, resulting in neck strain and pain between your shoulders.</p>
                <p class='articles-texto'>The neck problem you really don’t want is a herniated disk. That’s when there’s a rupture of the gelatinous disk between your vertebrae. When this gel bulges out from the disk, it can irritate the nerves. The problems will become evident over time. This is very painful and requires professional treatment to heal.</p>
                <p class='articles-texto'>Lastly, sleeping on your stomach causes your face to be squashed into your pillow or mattress, thus contributing to wrinkles.</p>
                <p class='articles-texto'>If this is the preferred position, at least put a pillow under the hips and lower abdomen to give the bottom of the spine a boost.</p>
                <p class='articles-texto'>A benefit of stomach sleeping is that is eases snoring and sleep apnea. And that is about it.</p>
                
                <h3 class='articles-subtitle'>Side Sleeping:</h3>
                <p class='articles-texto'>The vast majority of people sleep on their sides. This position can alleviate snoring and sleep apnea, ease heartburn and acid reflux, and relieve pressure on the lower back and hips, depending on body shape. Drawing your legs up slightly toward the chest and sleeping with a pillow between the knees can further reduce stress on the hips, back and spine.</p>
                <p class='articles-texto'>At the same time, resting your head on your arm can restrict blood flow and press down on the nerves, resulting in painful pins and needles. In this position, the shoulder supports most of the body’s weigh, which can constrict the neck and shoulder muscles. Also, sleeping on the left side can put pressure on the stomach and lungs (alternating sides often can help prevent organ strain).</p>
                
                <h3 class='articles-subtitle'>Back Sleeping:</h3>
                <p class='articles-texto'>Sleeping in savasana (corpse pose) is the most beneficial pose because the back is straight and not forced into any contortions. Plus, it allows you to experience zero-gravity position, at its best, if you have an adjustable base. It helps the mattress do its job of supporting your back by way of inner coils.</p>
                <p class='articles-texto'>Back sleeping allows the face to be out in the air and not smooshed up, which lead to fewer facial wrinkles.</p>
                <p class='articles-texto'>Snoring and sleep apnea are much more frequent. When sleeping on the back, gravity forces the base of the tongue to collapse into the airway, which obstructs breathing and creates the snore.</p>
                
                
                
