                <p class='articles-texto'>The National Sleep Foundation contents that along with the physical changes that occur as we get older, changes to our sleep patterns are a part of the normal aging process. As people age they tend to have a harder time falling asleep and more trouble staying asleep than when they were younger. It is a common misconception that sleep needs decline with age. In fact, research demonstrates that our sleep needs remain constant throughout adulthood. So, what's keeping seniors awake? Changes in the patterns of our sleep - what specialists call "sleep architecture" - occur as we age and this may contribute to sleep problems. Sleep occurs in multiple stages including dreamless periods of light and deep sleep, and occasional periods of active dreaming (REM sleep). The sleep cycle is repeated several times during the night and although total sleep time tends to remain constant, older people spend more time in the lighter stages of sleep than in deep sleep.</p>
                <p class='articles-texto'>Many older adults, though certainly not all, also report being less satisfied with sleep and more tired during the day. Studies on the sleep habits of older Americans show an increase in the time it takes to fall asleep (sleep latency), an overall decline in REM sleep, and an increase in sleep fragmentation (waking up during the night) with age. The prevalence of sleep disorders also tends to increase with age.</p>
                <p class='articles-texto'>A softer, nurturing mattress which envelopes the body can coax an elderly person to drift off into sleep. A properly light supportive mattress will keep the individual in sleep, coaxing them into and staying in REM sleep --- the most beneficial cycle of sleep. At the same time, lying on a flat mattresses contributes to discomfort and stress points. How often have we heard an elderly person say that they sleep better sitting in a reclining chair? An adjustable base will satisfy this requirement. The older body is riddled with arthritis, poor circulation, aches and pains, and an endless list of ailments. A stress relieving position --- zero gravity --- will relieve this pressure pain.</p>
                
