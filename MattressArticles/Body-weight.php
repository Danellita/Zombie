                <p class='articles-texto'>Body weight is the most crucial factor in determining mattress type. The proper “push back” from a coil mattress is required to keep pressure points stress-free. There is a direct correlation between weight and push back --- it is accomplishment by equal force. For example, a light weight person on a too firm mattress does not have enough force pushing down on the mattress to get good supportive push back from the coils in the negative areas of the body – the lumbar, the neck, between the shoulders and behind the knees.</p>
                <p class='articles-texto'>By the same token, a heavy person on a soft mattress will suppress coils, resulting in insufficient push back.</p>
                
