                <p class='articles-main-paragraph'>What is best for me? When you enter to a mattress store, you hear about what mattress are made of, what you should buy, and what is best for you...</p>
                
                <div class='articles-image'>
                    <img id='article2-image' src="/images/Articles-2.jpg" alt='selecting the best mattress'>
                </div>
                <p class='articles-texto'>...however, you cannot let so called experts decide for you. It is important to lie down on a mattress and decide whether you like it or not.</p>
                <p class='articles-texto'>Let's say you go into a shoe store. You like five pair of shoes, all by different manufacturers. You ask the salesperson to get you all five pairs in size 7.</p>
                <p class='articles-texto'>He brings you all five pairs in size 7. You try them all. But only one will fit you right, be comfortable and supportive.</p>
                <p class='articles-texto'>It is the exact same thing with mattresses.</p>

                <p class='articles-texto'>These are the most common materials:</p>

                <div class='articles-list'>
                    <ul>
                        <li><a href='#Foam'>Foam</a></li>
                        <li><a href='#Coil'>Coil</a></li>
                        <li><a href='#Latex'>Latex</a></li>
