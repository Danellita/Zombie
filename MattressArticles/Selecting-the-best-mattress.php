                <p class='articles-texto'>...and their pros and cons:</p>
                <div class="letter"><span id="Foam"></span>
                    <h3 class='articles-subtitle'>Foam:</h3>
                        <p class='articles-texto'>Foam has two advantages. It conforms to you and relieves pressure from the body.
                        But be careful. Some people don't like the sinking feeling. There is nothing wrong with the product,That is how it is suppose to function.</p>
                        <p class='articles-texto'>One of the biggest differences between foam mattress and coil mattress, is that in 
                        a foam mattress you sleep in the mattress, while on a coil mattress you sleep on it.</p>
                        <p class='articles-texto'>Sometimes people find it difficult to turn in foam mattress to change position. You have to be careful. Some foam mattresses get hot, depending on how they're manufactured, weather they have the cool gels and how the air flows through the mattress.</p>
                </div>
                <a href='#top'>Top</a>
                <div class="letter"><span id="Coil"></span>
                    <h3 class='articles-subtitle'>Coil:</h3>
                        <p class='articles-texto'>This is the type of mattress most people have been sleeping on, most of their lives.</p>
                        <p class='articles-texto'>A coil mattress is made of springs (coils) with different foams on top, depending on what kind of comfort level the manufacturer is trying to achieve.</p>
                </div>
                <a href='#top'>Top</a>
                <div class="letter"><span id="Latex"></span>
                    <h3 class='articles-subtitle' >Latex:</h3>
                        <p class='articles-texto'>You can get latex in natural form or man made synthetic.</p>
                        <p class='articles-texto'>Latex has pressure relieving properties like memory foam. However it is much faster reaction time than memory foam. Memory foam is slower moving when conforming to the body, and slower recovery time. Latex reacts quickly and recovers faster.</p>
                        <p class='articles-texto'>If you have latex allergy, you have to careful with this product.</p>
                </div>
                <a href='#top'>Top</a>
                <div class="letter"><span id="Air"></span>
                    <h3 class='articles-subtitle'>Air:</h3>
                        <p class='articles-texto'>Air mattress can be customized to individual comfort level. When two people have different comfort levels, they can customize each side of the bed, to fit their specific needs.</p>
                        <p class='articles-texto'>Inside the mattress are air chambers that can be filled or reduced of air, to change feel of the mattress.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="Hybrid"></span>
                    <h3 class='articles-subtitle'>Hybrid:</h3>
                        <p class='articles-texto'>Hybrid is a mattress in between a foam and coil mattress. The reason behind this mattress, is some people do not want the sinking feeling of a foam mattress, but want the benefits. The only way to achieve the feel of a coil mattress and benefits of a foam mattress was to design a hybrid mattress.</p>
                        <p class='articles-texto'>The hybrid mattress has coils like a regular mattress, but shorter in length. It has foam like a regular foam mattress. The difference is the base is made of coils, instead of foam so it gives you a different feeling.</p>
                </div>
