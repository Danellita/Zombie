                        <li><p class="school-texto">Sleep is just as important as diet and exercise. Good sleep improves cognition, concentration, productivity, memory performance and problem solving skills. Short sleep can negatively impact some aspects of brain function to a similar degree as alcohol intoxication. You simply cannot achieve optimal health without taking care of your sleep.</p></li>
                        <li><p class="school-texto">About 1 in 5 adults fail to get enough sleep. Unfortunately, the Western way of life is interfering with national sleep patterns; people are sleeping less than they did in the past, with decreased sleep quality.</p></li>
                        <li><p class="school-texto">Humans are the only mammals that willingly delay sleep. This self-inflicted deprivation reduces our ability to interact socially. Studies confirm that people who had not slept had a reduced ability to recognize expressions of anger and happiness, thus inhibiting us to process emotional information.</p></li>
