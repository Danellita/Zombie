                        <li><p class="school-texto">Poor sleep is linked to depression. Almost 90% of depression patients complain about sleep quality. Sleeping disorders, such as insomnia or obstructive sleep apnea are linked to depression and is even associated with increased risk of death by suicide.</p></li>
