                    <h3 class='FAQ-subtitle'>Is this a real sale or a fake sale?</h3>
                    <p class='articles-texto'>What is the original manufacturer’s suggested retail price? Is this in concert with other retailers? Is this a real “discount” or is it an artificially inflated price advertising a huge percentage discount? (For example, a retailer claims that a $1000 mattress is on sale for 50% off at $500 dollars. But the reality is that the mattress originally may have a price tag of $400 or $500 or $600 dollars. You may actually be paying more, paying the full retail price, or only getting a 10% discount). What is the actually bottom line price?</p>
                </div>
                    <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB2"></span>
                    <h3 class='FAQ-subtitle'>Will your retailer match other retailer’s advertised sale or on-line pricing for the exact same item?</h3>
                    <p class='articles-texto'>Retailers may have <i>“exclusives”</i> which simply means that they sell the same exact item under a different name exclusive for their establishment. It is the same concept as the pharmaceutical industry. There is the brand name with the high price tag, or the generic composition, which is a fraction of the cost. Can <i>“exclusives”</i> be the same mattress by a different name?</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB3"></span>
                    <h3 class='FAQ-subtitle'>What is a <i>“Low Price Guarantee”?</h3>
                        <div class='faq-image-block'>
                            <!--img class='faq-image1' src="/images/mattress_faq_info-how-much-sleep-web.jpg" alt='sleep disoder'-->
                        </div> 
                    <p class='articles-texto'>Apples-to-apples, a retailer may offer the lowest price off the Manufacturer’s Retail Price. If they do, you may ask the following: How long is this valid? Is it a limited time frame for the sale? Is it retro-active?</p>
                </div>
                <a href='#top'>Top</a>
                
                
                <div class="letter"><span id="BYB4"></span>
                    <h3 class='FAQ-subtitle'>Are there any <i>“Incentives or Freebies”</i> offered to make a purchase right now?</h3>
                        <p class='articles-texto'>Some manufacturers or retailers may offer gifts, such as a free bed frame, free pillows, free sheets, a free Fitbit or free TV.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB5"></span>
                    <h3 class='FAQ-subtitle'>Are there any special offers for USA veterans or seniors?</h3>
                        <p class='articles-texto'>Many establishments offer an additional discount. Don’t be shy about asking. Even if they don’t officially offer this, your sales associate may opt to give you an extra discount.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB6"></span>
                    <h3 class='FAQ-subtitle'>Is pricing negotiable? Does your retailer price match? </h3>
                        <div class='faq-image-block'>
                            <!--img class='faq-image2' src="/images/mattress_faq_info_apnea_web.jpg" alt='sleep disoder'-->
                        </div>
                        <p class='articles-texto'>Is it a brick-and-mortar match or an on-line match as well? Some retailers will only honor a physical store and not an on-line only company. The reason for this may be that an on-line operation may be providing older and not current models. It may be a floor sample or “slightly used” mattress. It may be another model.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB7"></span>
                    <h3 class='FAQ-subtitle'>What are the payment options?</h3>
                        <p class='articles-texto'>Will I get a better price if I open an account with the retailer? Can I finance my purchase, and if so, what is the interest rate? Many retailers offer an additional discount if you open an account or elect to open a short term line-of-credit with their company or affiliated bank. Make sure to find out what the terms are, what (if any) the finance charges are, if there is an interest rate, how long the term is, and if it based on price.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB8"></span>
                    <h3 class='FAQ-subtitle'>Is there a delivery charge?</h3>
                    <p class='articles-texto'>If there is a charge, what does it include? Do they set up your new mattress in your home or just drop ship to your door? Do they charge more for carry-ups?</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB9"></span>
                    <h3 class='FAQ-subtitle'>Is the old mattress set removed, and if so, is there a charge?</h3>
                    <p class='articles-texto'>Some companies may remove your old mattress set free-of-charge; however, there may be some stipulations, such as a soiled, torn or infected mattress. Health regulations may prohibit mattresses being removed if there is a presence of bed-bugs.</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB10"></span>
                    <h3 class='FAQ-subtitle'>What is the return or exchange policy?</h3>
                        <div class='faq-image-block'>
                            <!--img class='faq-image1' src="/images/mattress_faq_info_good_mattress-web.jpg" alt='good_mattress'-->
                        </div>
                        <p class='articles-texto'>Is there a time period for “testing” a mattress? If so, find out what it is. Determining a mattress’s benefit should never take more than 30 days. Companies that offer you an extended period of time may be hoping that you have gotten use to your new mattress and that you will not bother to exchange after this time period. If you deem that your mattress selection was not right, how long do you have to report it and how long do I have to choose another. Is there a charge to return or exchange a mattress?</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB11"></span>
                    <h3 class='FAQ-subtitle'>Will my new mattress feel the same at home as it does in the store?</h3>
                        
                    <p class='articles-texto'>There is a “breaking in” period for a mattress. It will be slightly firmer than the floor model, at first. So the relevant question becomes: How long has this mattress been on the sales floor?</p>
                </div>
                <a href='#top'>Top</a>
                
                <div class="letter"><span id="BYB12"></span>
                    <h3 class='FAQ-subtitle'>What is the mattress warranty?</h3>
                    <p class='articles-texto'>How many years is the mattress “guaranteed” for? Is it a full warranty or pro-rated? Is my purchase refundable or is it for a replacement only?</p>
                </div>
                <a href='#top'>Top</a>
                
