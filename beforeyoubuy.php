                    <h3 class='FAQ-subtitle'>Where is the mattress made? In the USA?</h3>
                        <p class='articles-texto'>You want a mattress made in the USA with safe and compliant regulations, so you can be confident that there is no off-gassing or fillers. The US has the most stringent health laws.</p>
                </div>
                <a href='#top'>Top</a>
                
