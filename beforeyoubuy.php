                    <h3 class='FAQ-subtitle'>What happens to a mattress when it is returned?</h3>
                        <p class='articles-texto'>Is it possible that I may receive a used or returned mattress? Does a returned mattress get re-sold? You want to make sure that you are getting a new mattress. This is the most intimate item that you will own and want to be confident that there is no contamination in any way with dust mites, lice, or bed bugs.</p>
                </div>
